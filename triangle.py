def area(a, h):
    #функция принимает длину основания и длину высоты, проведенной к этому основанию, треугольника и возвращает его площадь
    return a * h / 2

def perimeter(a, b, c):
    #функция принимает длины сторон треугольника (a, b, c) и возвращает его периметр
    return a + b + c
